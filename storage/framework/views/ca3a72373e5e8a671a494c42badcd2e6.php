			<div class="pb-md-5" style="text-align: justify;">
				<p>Mauban, home of Cagbalete, was initially  composed  of  five  small  settlements  known  as  Cagsiay,
					Daungang Dumagat,  Pinagbayanan,  Luya-luya  and  Tubog.   Daungang  Dumagat
					was  led  by  Gat Pagil,  a  Dumagat.  It   was  then  situated  at  what  
					today  is  the  pier  area.  On  the  other hand,  Pinagbayanan  that  was  
					located  at  the  side  of  Maapon  River  was  the  seat  of government  among 
					the  Tagalogs  led  by  Luis  Bernardo.  Both  communities  were  always subjected 
					to  raids  of  Moros  though  Daungang  Dumagat  through  their  great  warrior
					and leader  had  always  felt  a  sense  of  security.
					<br><br>
					Lack  of  road  access  kept  the  two  settlers  from  combining
					forces.  In  1678,  after  the demise  of  Luis  Bernardo,
					the  settlers  started  encountering  severe  and  ruthless 
					attacks from  the  Moros.   Baltazar  Palad,  Pinagbayanan’s 
					new  leader  joined  forces  with  Gat Pagil  by  relocating  
					near  Daungang  Dumagat.  Both  settled  in  an  area  which  they  
					called “Batis —  Malinaw”,  a  spring  that  produced  crystal  clear 
					water  and   the  place  where civilization  started  to  what  is  now
					Mauban.  Their  joining  of  forces  produced  peace  and order. 
					Later, people  from  Cavinti,  Laguna  and  other  nearby  towns  came  to 
					“Batis-Malinaw”  and  mingled  with  them  forming  a  new  community.
					<br><br>
					The  dissimilarity  of  the  way  of  life  of  the  new  settlers  with  
					the  group  generated disputes.  Because  of  that,  the  Dumagat  community 
					who  were  known  to  leave  in isolation  with  other  cultures  decided  to 
					leave  “Batis-Malinaw”  unnoticed  by  the  other settlers.  
					<br><br>
					Two  memories  about    Gat  Pagil  lingered  in  the  hearts  and  minds  of  the settlers,  
					one  was  his  military  genius  and  second  was   his  gray  hair  which  was  observed since  he  was  twenty  years  old.  
					Because  of  the  color  of  his  hair  he  was  fondly  called  Gat Uban  (“uban”  a  Tagalog  term  for  white  hair  and  believed  to  be  a  sign  of  wisdom) 
					which was  later  changed  to  Mauban  as  they  got  closer  to  him.  
					As  a  mark  of  respect  and  honor for  Gat  Pagil,  the  locality  was  called  Mauban.  
					Patterned  after  the  common  Spanish government   structure   throughout   the   Philippines,   Mauban   was   founded   as   a municipality  in  1583.
				</p> 
